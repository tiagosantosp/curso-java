package fundamentos;

public class TiposPrimitivos {
	public static void main(String[] args) {
		
		byte anosDeEmpresa = 127;
		System.out.println(anosDeEmpresa);
		short numeroDeVoos = 32000;
		System.out.println(numeroDeVoos);
		int id = 56789;
		System.out.println(id);
		//podemos separar as casas decimais com underline
		//para numeros LONG temos que colocar a letra L no final do numero
		long pontosAcumulados = 3_134_845_223L;
		System.out.println(pontosAcumulados);
		System.out.println();
		
		//temos que colocar a letra F no final de um numero float
		float salario = 11_45.44F;
		System.out.println(salario);
		double vendasAcumuladas = 2_991_797_103.01;
		System.out.println(vendasAcumuladas);
		System.out.println();
		
		
		boolean folga = true;
		System.out.println(folga);
		System.out.println();
		
		char sexo = 'M'; 
		System.out.println(sexo);
		
		
		
		
	}

}
