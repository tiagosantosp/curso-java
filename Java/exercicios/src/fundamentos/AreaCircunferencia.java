package fundamentos;

public class AreaCircunferencia {
	public static void main(String[] args) {
		// para declarar variavel colocamos o (tipo nomeVariavel e atribuição)  
		int raio = 6;		
		int raio2 = 7;
		// para declarar uma constante colocamos a palavra final  
		final double pi = 3.14159;
		
		System.out.println(pi * raio * raio2);
	}
}
